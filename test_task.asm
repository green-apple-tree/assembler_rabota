; --- ЭТАП 1: Инициализация данных (Вектор 1) ---
; Записываем значения, которые будем сдвигать.
; Используем адреса 10, 11, 12, 13, 14.

; Пример 1: Значение 383 (как в таблице примера LOAD в PDF)
LOAD 383
LOAD 10
WRITE 0     ; Mem[10] = 383

; Пример 2: Значение 88
LOAD 88
LOAD 11
WRITE 0     ; Mem[11] = 88

; Пример 3: Значение 1000
LOAD 1000
LOAD 12
WRITE 0     ; Mem[12] = 1000

; Пример 4: Значение 512
LOAD 512
LOAD 13
WRITE 0     ; Mem[13] = 512

; Пример 5: Значение 123
LOAD 123
LOAD 14
WRITE 0     ; Mem[14] = 123


; --- ЭТАП 2: Инициализация сдвигов (Вектор 2) ---
; Записываем величины, НА сколько будем сдвигать.
; Адреса: 20, 21, 22, 23, 24 (смещение 10 от данных).

LOAD 1      ; Сдвиг на 1 бит
LOAD 20
WRITE 0     ; Mem[20] = 1

LOAD 2      ; Сдвиг на 2 бита
LOAD 21
WRITE 0     ; Mem[21] = 2

LOAD 3      ; Сдвиг на 3 бита
LOAD 22
WRITE 0     ; Mem[22] = 3

LOAD 4      ; Сдвиг на 4 бита
LOAD 23
WRITE 0     ; Mem[23] = 4

LOAD 0      ; Сдвиг на 0 (проверка)
LOAD 24
WRITE 0     ; Mem[24] = 0


; --- ЭТАП 3: Выполнение операции SAR (Вариант 10) ---
; Логика команды SAR <Offset>:
; 1. Стек: Адрес (A)
; 2. Сдвиг берется из Mem[A + Offset]
; 3. Значение берется из Mem[A]
; 4. Результат пишется в Mem[A]
;
; Здесь Offset = 10.
; Если на стеке 10, сдвиг берется из (10+10)=20.

LOAD 10
SAR 10      ; Mem[10] = Mem[10] >> Mem[20] (383 >> 1 = 191)

LOAD 11
SAR 10      ; Mem[11] = Mem[11] >> Mem[21] (88 >> 2 = 22)

LOAD 12
SAR 10      ; Mem[12] = Mem[12] >> Mem[22] (1000 >> 3 = 125)

LOAD 13
SAR 10      ; Mem[13] = Mem[13] >> Mem[23] (512 >> 4 = 32)

LOAD 14
SAR 10      ; Mem[14] = Mem[14] >> Mem[24] (123 >> 0 = 123)

; --- Чтение результата (Опционально, для проверки READ) ---
; Чтобы убедиться, что READ работает как в задании
LOAD 10
READ        ; На стеке окажется 191